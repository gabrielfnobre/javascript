//A importância de funções em Javascript:

//O Javascript é uma linguagem onde praticamente vários elementos que temos são encarados como funções. Esse é um conceito muito importante de entendermos, afinal, para trabalhar com Javascript temos que entender muito bem o uso e aplicações das funções.
//Um bom exemplo de como as funções são importantes para essa linguagem é na interpretação de classes e objetos, enquanto classes e objetos são interpretados com o próprio tipo de elemento em algumas linguagens no Javascript classes e objetos são interpretados como "funções", veja só que surpresa.
//Vejamos alguns exemplos disso:

console.log(typeof Object); //Note que o tipo de um objeto é função!

class Produto {};
console.log(typeof Produto); //Note que até mesmo classes são encaradas como funções!