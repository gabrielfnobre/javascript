//MATH.MAX() E MATH.MIN():
//Os métodos "Math.max()" e "Math.min()" são métodos da função Math que são usados para retornar um número maior ou menor dentre um conjunto de números, basta nós colocamos o conjunto de números dentro do seu campo de parâmetros e o maior ou menor valor será retornado...

//RETORNANDO O MAIOR VALOR:
console.log('1)', Math.max(1, 2, 3, 4, 5))



//RETORNANDO O MENOR VALOR:
console.log('\n2)', Math.min(1, 2, 3, 4, 5))