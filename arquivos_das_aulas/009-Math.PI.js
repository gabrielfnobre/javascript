//O Math.PI é uma função built-in do JavaScript responsável por nos trazer o valor de PI automáticamente
//Vejamos como utilizá-la:

let raio = 10;
let calcArea = Math.PI * (raio * raio); //Perceba que em vez de colocar o valor, colocamos diretamente a função

console.log("O valor de uma área com raio de 10m é: " + calcArea + "m².");
console.log("O valor de Pi é: " + Math.PI + ".");